package Resources;

public class availableShows {
    private String screen_name;
    private String show_time;
    private String show_session;
    private String show_date;
    private String movie_name;
    private String show_id;
    private String multiplex_id;
    private int movie_id;

    public void setScreenName(String screen_name) {
        this.screen_name = screen_name;
    }

    public String getScreenName() {
        return screen_name;
    }

    public void setShowTime(String show_time) {
        this.show_time = show_time;
    }

    public String getShowTime() {
        return show_time;
    }

    public void setShowSession(String show_session) {
        this.show_session = show_session;
    }

    public String getShowSession() {
        return show_session;
    }

    public void setShowDate(String show_date) {
        this.show_date = show_date;
    }

    public String getShowDate() {
        return show_date;
    }

    public void setMovieName(String movie_name) {
        this.movie_name = movie_name;
    }

    public String getMovieName() {
        return movie_name;
    }

    public void setShowId(String show_id) {
        this.show_id = show_id;
    }

    public String getShowId() {
        return show_id;
    }

    public void setMultiplexId(String multiplex_id) {
        this.multiplex_id = multiplex_id;
    }

    public String getMultiplexId() {
        return multiplex_id;
    }

    public void setMovieId(int movie_id) {
        this.movie_id = movie_id;
    }

    public int getMovieId() {
        return movie_id;
    }
}
